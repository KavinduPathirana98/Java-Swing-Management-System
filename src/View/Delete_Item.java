    void view() throws Exception
{
    try
        {
         
       Connection con = DB.createConnection();
                Statement st = con.createStatement();
                String sql = "select * from products ";
                 rst=st.executeQuery(sql);
         
         DefaultTableModel model=new DefaultTableModel(new String[]{"ID","Name","Item_code","World_unique","Branch_unique","Engine_type","Pack_size","Quantity","Buying_pric","Selling_price","Profit","Category"},0);
         
          while (rst.next()){
         Vector vec = new Vector();
         
         vec.add(rst.getString("ID"));
         vec.add(rst.getString("Name"));
         vec.add(rst.getString("Item_code"));
         vec.add(rst.getString("World_unique"));
         vec.add(rst.getString("Branch_unique"));
         vec.add(rst.getString("Engine_type"));
         vec.add(rst.getString("Pack_size"));
         vec.add(rst.getString("Quantity"));
         vec.add(rst.getString("Buying_price"));
         vec.add(rst.getString("Selling_price"));
         vec.add(rst.getString("Profit"));
         vec.add(rst.getString("Category"));
        
         model.addRow(vec);
         table.setModel(model);
        
          
         }
         
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Database Error !!");
        }
}
    void search() throws Exception
{
    try
        {
         
       Connection con = DB.createConnection();
                Statement st = con.createStatement();
                String sql = "select ID,Name,Item_code,World_unique,Branch_unique,Engine_type,Pack_size,Quantity,Selling_price,Category from products where name like '%"+txt_search.getText()+"%' or world_unique like '%"+txt_search.getText()+"%' or branch_unique like  '%"+txt_search.getText()+"%'";
        ResultSet rst = st.executeQuery(sql);
         
         DefaultTableModel model=new DefaultTableModel(new String[]{"ID","Name","Item_code","World_unique","Branch_unique","Engine_type","Pack_size","Quantity","Selling_price","Category"},0);
         
          while (rst.next()){
         Vector vec = new Vector();
         
         vec.add(rst.getString("ID"));
         vec.add(rst.getString("Name"));
         vec.add(rst.getString("Item_code"));
         vec.add(rst.getString("World_unique"));
         vec.add(rst.getString("Branch_unique"));
         vec.add(rst.getString("Engine_type"));
         vec.add(rst.getString("Pack_size"));
         vec.add(rst.getString("Quantity"));
         
         vec.add(rst.getString("Selling_price"));
       
         vec.add(rst.getString("Category"));
        
         model.addRow(vec);
         table.setModel(model);
        
          
         }
         
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,ex);
        }
}
    void delete_Item()
{
    try 
            {
                Connection con = DB.createConnection();
                String sql = "delete from products where ID='"+txt_id.getText()+"'";
                PreparedStatement stmt = con.prepareStatement(sql);
                int i = stmt.executeUpdate();

                if (i == 1) 
                {
                    JOptionPane.showMessageDialog(this, "Successfully Deleted the Item.", "Done", JOptionPane.PLAIN_MESSAGE);
                }
                else
                {
                    JOptionPane.showMessageDialog(this,"Cannot Delete the Item. Try Again","Error  ",JOptionPane.ERROR_MESSAGE);
                }

            } 
            
            catch(SQLException e)
            {
                JOptionPane.showMessageDialog(this,"Database Error. Please Try Again","Database Error",JOptionPane.ERROR_MESSAGE);
            }
            catch (Exception e) 
            {
                JOptionPane.showMessageDialog(this,"Please Try Again","Error  " + e,JOptionPane.ERROR_MESSAGE);
            }
}

